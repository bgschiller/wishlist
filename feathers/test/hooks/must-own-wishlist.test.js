const feathers = require('@feathersjs/feathers');
const mustOwnWishlist = require('../../src/hooks/must-own-wishlist');

describe('\'must-own-wishlist\' hook', () => {
  let app;

  beforeEach(() => {
    app = feathers();

    app.use('/dummy', {
      async get(id) {
        return { id };
      }
    });

    app.service('dummy').hooks({
      before: mustOwnWishlist()
    });
  });

  it('runs the hook', async () => {
    expect.assertions(1);
    const result = await app.service('dummy').get('test');
    expect(result).toEqual({ id: 'test' });
  });
});
